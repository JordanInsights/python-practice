#Arithmetic operators are what you'd expect
a = 5
b = 10
print()
print('a = 5', 'b=10')
print()

print('a+b = ', a+b)
print('a*b = ', a*b)
print('a-b = ', a-b)
print('a/b = ', a/b)
print()

# Modulo does remainder as per
print('a%b = ', a%b)
print() 

# Equal to is ==
print('a == b', a == b)
print()

# 'and' and 'or' operators are simply the words
print('5 > 2 and 10 > 5', 5 > 2 and 10 > 5)
print('5 > 2 or 3 > 5', 5 > 2 or 3 > 5)
print()

# can reverse results using 'not'
print('not(50 > 5)', not(50 > 5))